// and format in java
class format
{
public static void main(String[] args)
{
String s="syamala";
int a=55;
float b=4.4f;
double pi=3.1416;
System.out.format("%3s",s);
System.out.format("%5d",a);
System.out.format("%10f",b);
System.out.format("%10.2f",pi);               //.2f means after decimal it prints 2 decimal places only
}
}
