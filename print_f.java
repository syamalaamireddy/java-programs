//printf function and format in java
class print_f
{
public static void main(String[] args)
{
String s="syamala";
int a=55;
float b=4.4f;
double pi=3.1416;
System.out.printf("%3s",s);
System.out.printf("%5d",a);
System.out.printf("%10f",b);
System.out.printf("%10.2f",pi);               //.2f means after decimal it prints 2 decimal places only
}
}
